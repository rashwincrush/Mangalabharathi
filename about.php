<script>
// Counter Animation
function animateCounter(element) {
    const target = parseInt(element.getAttribute('data-count'));
    const duration = 2000;
    const step = target / (duration / 16);
    let current = 0;
    
    const timer = setInterval(() => {
        current += step;
        if (current >= target) {
            element.textContent = target + '+';
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(current) + '+';
        }
    }, 16);
}

// Intersection Observer for Counters
const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.1
};

const counterObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const counters = entry.target.querySelectorAll('.impact-number');
            counters.forEach(counter => animateCounter(counter));
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

// Timeline Animation
const timelineObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('timeline-visible');
        }
    });
}, observerOptions);

// Activity Cards Animation
const cardObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('card-visible');
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

// Initialize on DOM Load
document.addEventListener('DOMContentLoaded', () => {
    // Initialize Counter Animation
    const impactMetrics = document.querySelector('.impact-metrics');
    if (impactMetrics) {
        counterObserver.observe(impactMetrics);
    }

    // Initialize Timeline Animation
    const timelineItems = document.querySelectorAll('.timeline-item');
    timelineItems.forEach(item => {
        timelineObserver.observe(item);
    });

    // Initialize Card Animations
    const cards = document.querySelectorAll('.activity-card, .mission-card, .vision-card');
    cards.forEach(card => {
        cardObserver.observe(card);
    });

    // Initialize Memory Profile Hover Effects
    const memoryProfiles = document.querySelectorAll('.memory-profile');
    memoryProfiles.forEach(profile => {
        profile.addEventListener('mouseenter', () => {
            profile.classList.add('profile-hover');
        });
        profile.addEventListener('mouseleave', () => {
            profile.classList.remove('profile-hover');
        });
    });

    // Initialize Smooth Scrolling
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
});
</script>

